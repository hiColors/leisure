package com.github.life.lab.leisure.common.utils;

public class A {

    private Long id;

    private String name;

    private Integer age;

    public Long getId() {
        return id;
    }

    public A setId(Long id) {
        this.id = id;
        return this;
    }

    public String getName() {
        return name;
    }

    public A setName(String name) {
        this.name = name;
        return this;
    }

    public Integer getAge() {
        return age;
    }

    public A setAge(Integer age) {
        this.age = age;
        return this;
    }
}
